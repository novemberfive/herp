export function expandPermissions(permissions = []) {
  const merged = new Set(permissions.filter(Boolean))
  merged.forEach(permission => {
    const implied = PERMISSION_IMPLICATIONS[permission] || []
    implied.forEach(item => merged.add(item))
  })
  return Array.from(merged)
}
