<script setup>
import { ref, onMounted } from 'vue'
import { getStats } from '@/api/asset'

const stats = ref({ total: 0, statusCounts: [] })

const getStatusCount = (status) => {
  const item = stats.value.statusCounts?.find(s => s.status === status)
  return item?.count || 0
}

const fetchStats = async () => {
  try {
    const res = await getStats()
    stats.value = res.data
  } catch (error) {
    console.error(error)
  }
}

onMounted(() => {
  fetchStats()
})
</script>

